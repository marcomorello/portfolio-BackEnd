package com.portfolio.mm.Security.Controller;

public class Mensaje {
	private String mensaje;


	//Constructors
	
	public Mensaje() {}
	
	public Mensaje(String mensaje) {
		super();
		this.mensaje = mensaje;
	}


	//Getter y setter
	public String getMensaje() {
		return mensaje;
	}

	public void setMensaje(String mensaje) {
		this.mensaje = mensaje;
	}
	
	
	
}
